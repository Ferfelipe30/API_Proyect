--Tabla Personas
CREATE TABLE T001Persona (
	T001IdPersona SERIAL PRIMARY KEY,
	T001Nombre VARCHAR(100) NOT NULL,
	T001Apellido VARCHAR(100) NOT NULL,
	T001Documento VARCHAR(50) UNIQUE NOT NULL,
	T001Email VARCHAR(150) UNIQUE NOT NULL,
	T001Activo BOOLEAN DEFAULT TRUE
);

--Tabla Tarea
CREATE TABLE T002Tarea (
	T002IdTarea SERIAL PRIMARY KEY,
	T002Titulo VARCHAR (200) NOT NULL,
	T002FechaLimite DATE,
	T002PersonaId INT NOT NULL,

	CONSTRAINT fk_persona FOREIGN KEY (T002PersonaId) REFERENCES T001Persona(T001IdPersona) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE INDEX idx_persona_documento ON T001Persona(T001Documento);
CREATE INDEX idx_persona_email ON T001Persona(T001Email);

CREATE INDEX idx_tarea_persona ON T002Tarea(T002PersonaId);